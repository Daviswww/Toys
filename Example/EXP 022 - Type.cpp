#include<bits/stdc++.h>

using namespace std;

int main()
{
	//unsigned 
	int tp1 = 10;
	printf("int =  %d\n", tp1);
	
	long long int tp2;
	printf("long long int =  %lld\n", tp2);

	float tp3 = 10.5;
	printf("float = %f\n", tp3);

	double tp4 = 10.55555555;
	printf("double = %lf\n", tp4);

	char tp5 = 'C';
	printf("char = %c\n", tp5);	
	
	string str = "Hello!";
	cout << str << endl;
	
	bool bo1 = true, bo2 = false;
	cout << "True(bo1) : "<< bo1 << " False(bo2) : "<<bo2 <<endl;
	
	return 0;	
} 
